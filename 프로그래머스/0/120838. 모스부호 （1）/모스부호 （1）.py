def solution(letter):
    # 모스 부호와 알파벳을 매핑하는 딕셔너리 생성
    morse = {
        '.-':'a','-...':'b','-.-.':'c','-..':'d','.':'e','..-.':'f',
        '--.':'g','....':'h','..':'i','.---':'j','-.-':'k','.-..':'l',
        '--':'m','-.':'n','---':'o','.--.':'p','--.-':'q','.-.':'r',
        '...':'s','-':'t','..-':'u','...-':'v','.--':'w','-..-':'x',
        '-.--':'y','--..':'z'
    }

    # 입력된 모스 부호 문자열을 공백(' ')을 기준으로 분리하여 리스트로 저장
    letter = letter.split(' ')

    # 변환된 알파벳을 저장할 리스트 초기화
    answer = []

    # 분리된 모스 부호를 하나씩 반복하여 변환
    for i in letter:
        answer.append(morse[i])  # 해당 모스 부호를 딕셔너리에서 찾아 변환 후 리스트에 추가

    # 리스트에 저장된 알파벳들을 문자열로 합쳐 최종 결과 반환
    return ''.join(answer)

# 예제 실행 (출력: "hello")
print(solution(".... . .-.. .-.. ---"))

# 예제 실행 (출력: "python")
print(solution(".--. -.-- - .... --- -."))